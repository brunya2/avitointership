import UIKit

extension UIFont {
    class func notoSansKannadaRegular(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "NotoSansKannada-Regular", size: size)!
    }
    class func notoSansKannadaBold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "NotoSansKannada-Bold", size: size)!
    }
}
